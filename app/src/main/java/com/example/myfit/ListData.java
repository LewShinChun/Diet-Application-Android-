package com.example.myfit;

public class ListData {

    String name, time, calories;
    int ingredients, desc;
    int image;

    public ListData(String name, String time, String calories, int ingredients, int desc, int image) {
        this.name = name;
        this.time = time;
        this.calories = calories;
        this.ingredients = ingredients;
        this.desc = desc;
        this.image = image;

    }
}
